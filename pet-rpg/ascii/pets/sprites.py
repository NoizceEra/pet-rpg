def get_sprite(species: str, stage: str, path: str | None = None, mood: str = "normal") -> str:
    """
    Retrieve a sprite for the given species / stage / path combination.

    Lookup priority:
      1. {STAGE}_{PATH}_{MOOD}
      2. {STAGE}_{PATH}
      3. {STAGE}_{MOOD}
      4. {STAGE}
      5. BABY (last-resort fallback)
    """
    sprites = SPECIES_SPRITES.get(species, SPECIES_SPRITES["MoltCrab"])

    candidates = []
    if path:
        candidates.append(f"{stage}_{path}_{mood}")
        candidates.append(f"{stage}_{path}")
    candidates.append(f"{stage}_{mood}")
    candidates.append(stage)
    candidates.append("BABY")  # last resort

    for key in candidates:
        if key in sprites:
            return sprites[key]

    return "\n  [???]\n"
